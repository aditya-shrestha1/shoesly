class CartModel {
  final String productName;
  final String brandName;
  final String color;
  final String size;
  final double price;
  final int quantity;
  final String productImage;

  CartModel({
    required this.productName,
    required this.brandName,
    required this.color,
    required this.size,
    required this.price,
    required this.quantity,
    required this.productImage,
  });
}
